#!/usr/bin/env python3
"""Generate compact default-casing tables from the pinned Unicode data."""

from pathlib import Path


ROOT = Path(__file__).resolve().parent
DATA = ROOT / "Data" / "17.0.0"
OUTPUT = ROOT / "CaseData.hpp"


def scalars(field: str) -> tuple[int, ...]:
    return tuple(int(value, 16) for value in field.strip().split())


def special_mappings() -> tuple[list[tuple[int, tuple[int, ...]]], list[tuple[int, tuple[int, ...]]]]:
    lower: list[tuple[int, tuple[int, ...]]] = []
    upper: list[tuple[int, tuple[int, ...]]] = []
    for raw_line in (DATA / "SpecialCasing.txt").read_text(encoding="utf-8").splitlines():
        line = raw_line.split("#", 1)[0].strip()
        if not line:
            continue
        fields = [field.strip() for field in line.split(";")]
        if fields[4]:
            continue
        code = int(fields[0], 16)
        lower_mapping = scalars(fields[1])
        upper_mapping = scalars(fields[3])
        if lower_mapping != (code,):
            lower.append((code, lower_mapping))
        if upper_mapping != (code,):
            upper.append((code, upper_mapping))
    lower.sort(key=lambda item: item[0])
    upper.sort(key=lambda item: item[0])
    return lower, upper


def property_ranges(name: str) -> list[tuple[int, int]]:
    result: list[tuple[int, int]] = []
    for raw_line in (DATA / "DerivedCoreProperties.txt").read_text(encoding="utf-8").splitlines():
        line = raw_line.split("#", 1)[0].strip()
        if not line:
            continue
        code_field, property_name = [field.strip() for field in line.split(";", 1)]
        if property_name != name:
            continue
        bounds = code_field.split("..")
        first = int(bounds[0], 16)
        last = int(bounds[-1], 16)
        result.append((first, last))
    return result


def format_values(name: str, mappings: list[tuple[int, tuple[int, ...]]]) -> tuple[str, list[tuple[int, int, int]]]:
    values: list[int] = []
    records: list[tuple[int, int, int]] = []
    for code, mapping in mappings:
        offset = len(values)
        values.extend(mapping)
        records.append((code, offset, len(mapping)))
    lines = [f"inline constexpr std::uint32_t {name}[] = {{"]
    for index in range(0, len(values), 8):
        chunk = ", ".join(f"0x{value:04X}" for value in values[index:index + 8])
        lines.append(f"    {chunk},")
    lines.append("};")
    return "\n".join(lines), records


def format_mappings(name: str, records: list[tuple[int, int, int]]) -> str:
    lines = [f"inline constexpr CaseMapping {name}[] = {{"]
    lines.extend(
        f"    {{0x{code:04X}, {offset}, {length}}},"
        for code, offset, length in records
    )
    lines.append("};")
    return "\n".join(lines)


def format_ranges(name: str, ranges: list[tuple[int, int]]) -> str:
    lines = [f"inline constexpr CodepointRange {name}[] = {{"]
    lines.extend(f"    {{0x{first:04X}, 0x{last:04X}}}," for first, last in ranges)
    lines.append("};")
    return "\n".join(lines)


def main() -> None:
    lower, upper = special_mappings()
    lower_values, lower_records = format_values("lowercaseValues", lower)
    upper_values, upper_records = format_values("uppercaseValues", upper)
    content = f"""// Generated by generate_case_data.py from Unicode 17.0.0.
// Do not edit manually. Source files carry the Unicode data license.
#pragma once

#include <cstddef>
#include <cstdint>

namespace silex::unicode {{

struct CaseMapping {{
    std::uint32_t codepoint;
    std::uint16_t offset;
    std::uint8_t length;
}};

struct CodepointRange {{
    std::uint32_t first;
    std::uint32_t last;
}};

{lower_values}

{format_mappings("lowercaseMappings", lower_records)}

{upper_values}

{format_mappings("uppercaseMappings", upper_records)}

{format_ranges("casedRanges", property_ranges("Cased"))}

{format_ranges("caseIgnorableRanges", property_ranges("Case_Ignorable"))}

}} // namespace silex::unicode
"""
    OUTPUT.write_text(content, encoding="utf-8")


if __name__ == "__main__":
    main()
